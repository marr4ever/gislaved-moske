// Helper function to convert the prayer times data to DailyPrayers format
export function convertToDailyPrayers(date: Date): DailyPrayers | null {
  const dateString = date.toISOString().split("T")[0]
  const prayerTimes = prayerTimesData[dateString]

  if (!prayerTimes) {
    return null
  }

  const weekday = date.toLocaleDateString("sv-SE", { weekday: "long" })
  const hijriDate = hijriDates[dateString] || ""

  return {
    date: dateString,
    weekday: weekday,
    weekdayArabic: getArabicWeekdayFromDate(date),
    hijriDate: hijriDate,
    prayers: prayerTimes.map((prayer) => ({
      name: prayer.name,
      time: prayer.time,
      arabicName: prayer.arabicName,
      swedishName: getSwedishPrayerName(prayer.name),
    })),
  }
}

// Helper function to get Arabic weekday from date
function getArabicWeekdayFromDate(date: Date): string {
  const weekdayMap: Record<number, string> = {
    0: "الأحد", // Sunday
    1: "الاثنين", // Monday
    2: "الثلاثاء", // Tuesday
    3: "الأربعاء", // Wednesday
    4: "الخميس", // Thursday
    5: "الجمعة", // Friday
    6: "السبت", // Saturday
  }

  return weekdayMap[date.getDay()]
}

// Helper function to get Swedish prayer name
function getSwedishPrayerName(name: string): string {
  const nameMap: Record<string, string> = {
    Fajr: "Fajr",
    Sunrise: "Soluppgång",
    Dhuhr: "Dhuhr",
    Asr: "Asr",
    Maghrib: "Maghrib",
    Isha: "Isha",
  }

  return nameMap[name] || name
}

// Helper function to get all prayer times for a month
export function getMonthlyPrayerTimes(year: number, month: number): DailyPrayers[] {
  const result: DailyPrayers[] = []
  const daysInMonth = new Date(year, month, 0).getDate()

  for (let day = 1; day <= daysInMonth; day++) {
    const date = new Date(year, month - 1, day)
    const dailyPrayers = convertToDailyPrayers(date)

    if (dailyPrayers) {
      result.push(dailyPrayers)
    }
  }

  return result
}

// Helper function to check if we have data for a specific date
export function hasDataForDate(date: Date): boolean {
  const dateString = date.toISOString().split("T")[0]
  return dateString in prayerTimesData
}

// Helper function to get the date range for which we have data
export function getDataDateRange(): { start: Date; end: Date } {
  const dates = Object.keys(prayerTimesData).sort()

  if (dates.length === 0) {
    return { start: new Date(), end: new Date() }
  }

  return {
    start: new Date(dates[0]),
    end: new Date(dates[dates.length - 1]),
  }
}
